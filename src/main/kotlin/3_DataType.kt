fun main(args: Array<String>) {
    println("3_DataTypes")

    // Integer Type
    val maxIntegerValue: Int = Int.MAX_VALUE
    val minIntegerValue: Int = Int.MIN_VALUE

    println("Max Int value $maxIntegerValue\nMin Int value $minIntegerValue")

    // Byte Type
    val maxByteValue: Byte = Byte.MAX_VALUE
    val minByteValue: Byte = Byte.MIN_VALUE

    println("Max Byte value $maxByteValue\nMin Byte value $minByteValue")

    // Short Type
    val maxShortValue: Short = Short.MAX_VALUE
    val minShortValue: Short = Short.MIN_VALUE

    println("Max Byte value $maxShortValue\nMin Short value $minShortValue")

    // Long Type
    val maxLongValue: Long = Long.MAX_VALUE
    val minLongValue: Long = Long.MIN_VALUE

    println("Max Long value $maxLongValue\nMin Short value $minLongValue")

}